#include "libft.h"

size_t		ft_strcchr(const char *str, int c)
{
	int i;
	int k;

	i = 0;
	k = 0;
	while (str[i])
	{
		if (str[i] == (char)c)
			k++;
		i++;
	}
	return (k);
}
