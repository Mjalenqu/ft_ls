#include "libft.h"

void	ft_arrstrdel(char **as)
{
	int i;

	if (!(as))
		return ;
	i = 0;
	while (as[i])
	{
		free(as[i]);
		i++;
	}
	free(as[i]);
	free(as);
	as = NULL;
}
